        <p>The English Broadside Ballad Archive digitizes, transcribes, and
        creates analytical tools for over 9,000 seventeenth-century ballads. At
        UC Santa Barbara, I worked at all levels of the project, from data
        entry to project management; notable work from this time includes
        developing a tag set for EBBA's woodcut images. We use these tags in
        concert with <a href="https://ebba.english.ucsb.edu/page/20">feature
        extraction methods</a> to make woodcuts searchable on our website. At
        UC Davis, I have helped migrate EBBA to a new website, which integrates
        close and distant reading methods by featuring word/tf-idf clouds,
        document topic histograms, and bigram networks for each ballad (example
        <a href="https://ebba.english.ucsb.edu/ballad/20053/tm">here</a>).
        Currently, I am developing methods to automatically identify ballad
        reprints in the archive. This is part of a push to make EBBA accord
        with linked open data standards: canonical versions of ballads will be
        assigned uniform resource identifiers and integrated with the <a
        href="https://mapoflondon.uvic.ca/">Map of Early Modern London</a> and
        <a href="http://quintessence.ds.lib.ucdavis.edu/">Quintessence</a>, a
        corpus exploration tool for the Early English Books Online
        collection.</p>
