        <h4>3D printing</h4>
        <p>My interests in comparative textual media led me to take a hybrid
        approach to print history at the Maker Lab. For example, I have created
        3D-printed objects that can be typeset in "Mad Madge," the Lab's
        replica nineteenth-century Albion pull press. I also acquired Linotype
        matrices for the Lab, which have helped myself and others at UCSB make
        connections between hot-metal printing and the extruded objects of 3D
        printers. This hybrid approach is a way of thinking in practice about
        how we can situate the many recurrences and continuities in print
        history within the context of text’s digital production and circulation
        today. When discussing that laminate in Maker Lab workshops, it has
        also been a key way of speaking across disciplinary boundaries. The
        weird mimicry of a 3D-printed "woodcut" highlights medial hybridity (or
        what Jay David Bolter and Richard Grusin [1999] would call
        "remediation"); understanding that hybridity in the context of the
        Maker Lab has meant soliciting the expertise of participants with
        backgrounds in book art, film and media studies, creative coding, and
        early modern literature.</p>
        <hr>
        <h4>Linotext Minima</h4>
        <p>Thinking in practice at the Maker Lab has led me to value making
        "gestures": small, sometimes simple exercises that attempt to
        illuminate or otherwise operationalize a technology, formal technique,
        or theoretical concept. I often use gestures in my teaching, though
        they have also helped me conduct my own dissertation research. I found
        it difficult, for example, to keep the complicated encoding scheme
        Linotypes use to redistribute their matrices in my head, so while
        working on these machines I visualized their 91 different matrices.
        This provided me with a concrete representation of how Linotypes "read"
        an <i>e</i> differently than <i>W</i>.</p>
