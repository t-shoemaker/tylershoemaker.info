        <h3>.notyetdef (2019)</h3>
        <p>This collaboration with <a href="http://fredbenenson.com/"
        target="_blank">Fred Benenson</a> trains a generative adversarial
        network on Unicode-enabled font indexes to create hybrid glyphs from
        extant writing systems. These new glyphs serve as a vantage from which
        we are able to survey font internationalization and global script
        representation in computing. The Unicode standard unifies the world's
        writing systems with a peculiar shortcut: while it maintains address
        locations, or "code points," for some 137,993 characters, it does not
        specify what those characters should look like when a user's computer
        displays them onscreen. More, there is no way to download "all of
        Unicode": no usable archive of its characters exists. In response, we
        have pieced together our own archive of font indexes and generated new
        glyphs therefrom, using these hybrids to reflect on the standard's
        still-outstanding absences and the thousands of code points it has yet
        to fill. Our future plans include investigating how machine learning
        methods can indicate the influence of typographic font conventions in
        digital renditions of script systems like Bengali, Hanzi, and so
        on.</p>
