        <p>wateReview is a corpus exploration platform that supports a
        data-driven literature review of water management in the Caribbean and
        South and Central America (titled <a
        href="https://hess.copernicus.org/preprints/hess-2021-106/"
        target="_blank">"Bright and Blind Spots of Water Research in Latin
        America and the Caribbean"</a>). Working in collaboration with
        wateReview's domain experts, I built this platform and its many
        interactive visualizations, which include a topic model viewer, network
        connectivity graphs, and several chord diagrams. Central to our design
        considerations was feedback we received from three workshops held in
        2020, where we discussed various needs from and for the water research
        community. These workshops were held in the three major languages of
        the Caribbean and South and Central America: English, Spanish, and
        Portuguese. wateReview, in turn, is a multilingual site that offers
        several multifaceted views of the corpus, ranging from overarching
        topic distribution plots to individualized "country cards" that break
        down specific research metrics for each country we studied.</p>
