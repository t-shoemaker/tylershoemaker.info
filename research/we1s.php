        <hr>
        <p>WhatEvery1Says (WE1S) studies public discourse about the humanities,
        using topic models to analyze a corpus comprised of several hundred
        thousand news articles. During the project's first year, I assisted in
        developing a framework for corpus design that we call <a
        href="https://we1s.ucsb.edu/research_post/scoping-representativeness/">
        "representativeness"</a>: an iterative approach to what our corpus
        represents that works in recognition of the ways bias can inhere in
        data collection. Later involvements included hand labeling articles for
        text classification experiments and optimizing topic models for human
        readability using perplexity scores. I spent my final months with WE1S
        examining how data preparation sometimes replicates structures of
        power, flattening the diversity that our concept of representativeness
        tries to foreground. Initial reporting on this problem may be found in
        a special <a
        href="https://we1s.ucsb.edu/research/we1s-methods/key-methods/">"Methods
        Issues & Limitations"</a> section on the WE1S website; there, my
        contributions include a report on <a
        href="https://we1s.ucsb.edu/wp-content/uploads/M-100.pdf">how topic
        models tend to elide political differences</a> and a collaboratively
        written reflection on <a
        href="https://we1s.ucsb.edu/wp-content/uploads/M-101.pdf">why word
        order matters</a>.</p>
